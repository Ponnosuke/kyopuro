/*
char型の変数に cin で入力すると一文字ずつ取り出すことができる。
*/

#include <bits/stdc++.h>
using namespace std;

using i64 = int64_t;
using ui64 = uint64_t;

int main() {
    char a, b, c, d, e;
    cin >> a >> b >> c >> d >> e;  // Hello と入力

    cout << a << endl;  // H
    cout << b << endl;  // e
    cout << c << endl;  // l
    cout << d << endl;  // l
    cout << e << endl;  // o
}
