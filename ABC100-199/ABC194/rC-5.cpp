int main() {
    i64 N;
    cin >> N;
    i64 sum1 = 0, sum2 = 0;
    for(int i = 0; i < N; i++){
        i64 A;
        cin >> A;
        sum1 += A*A;
        sum2 += A;
    }

    cout << N*sum1 - sum2*sum2 << '\n';
}